alert(`A subtração de ${n1} e ${n2} é igual a ${n1 - n2}`)
alert(`A divisão de ${n1} e ${n2} é igual a ${n1 / n2}`)
alert(`A multiplicação de ${n1} e ${n2} é igual a ${n1 * n2}`)
